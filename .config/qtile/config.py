import os
import subprocess
from libqtile import layout, hook
from libqtile.config import Screen, Click, Drag, Group, Key, Match
from libqtile.lazy import lazy

mod = "mod4"
terminal = "kitty"

keys = [
    # A list of available commands that can be bound to keys can be found
    # at https://docs.qtile.org/en/latest/manual/config/lazy.html
    # Switch between windows
    Key([mod], "h", lazy.layout.left(), desc="focus left"),
    Key([mod], "l", lazy.layout.right(), desc="focus right"),
    Key([mod], "j", lazy.layout.down(), desc="focus down"),
    Key([mod], "k", lazy.layout.up(), desc="focus up"),
    Key([mod], "space", lazy.layout.next(), desc="focus next"),

    # Window moves
    Key([mod, "shift"], "h", lazy.layout.shuffle_left(), desc="Left"),
    Key([mod, "shift"], "l", lazy.layout.shuffle_right(), desc="Right"),
    Key([mod, "shift"], "j", lazy.layout.shuffle_down(), desc="Down"),
    Key([mod, "shift"], "k", lazy.layout.shuffle_up(), desc="Up"),
    # Grow windows. If current window is on the edge of screen and direction
    # will be to screen edge - window would shrink.
    Key([mod, "control"], "h", lazy.layout.grow_left(), desc="Grow left"),
    Key([mod, "control"], "l", lazy.layout.grow_right(), desc="Grow right"),
    Key([mod, "control"], "j", lazy.layout.grow_down(), desc="Grow down"),
    Key([mod, "control"], "k", lazy.layout.grow_up(), desc="Grow up"),
    Key([mod], "n", lazy.layout.normalize(), desc="Reset all sizes"),
    Key([mod], 'period', lazy.next_screen(), desc='Next monitor'),
    Key([mod], 'comma', lazy.prev_screen(), desc='Next monitor'),
    # Toggle between split and unsplit sides of stack.
    # Split = all windows displayed
    # Unsplit = 1 window displayed, like Max layout, but still with
    # multiple stack panes
    Key(
        [mod, "shift"],
        "Return",
        lazy.layout.toggle_split(),
        desc="Toggle between split and unsplit sides of stack",
    ),
    Key([mod], "Return", lazy.spawn(terminal), desc="Launch terminal"),
    # Toggle between different layouts as defined below
    Key([mod], "Tab", lazy.next_layout(), desc="Toggle between layouts"),
    Key([mod], "w", lazy.window.kill(), desc="Kill focused window"),
    Key([mod, "control"], "r", lazy.reload_config(), desc="Reload the config"),
    Key([mod, "control"], "q", lazy.shutdown(), desc="Shutdown Qtile"),
    Key([mod], "d",
        lazy.spawn("rofi -modi drun -show drun -config ~/.config/rofi/rofidmenu.rasi"),
        desc="Spawn a command using a prompt widget"),

    Key([mod], "comma", lazy.to_screen(0), desc='Focus monitor 1'),
    Key([mod], "period", lazy.to_screen(1), desc='Focus monitor 2'),
]


groups = [Group(i) for i in "123456789"]

for i in groups:
    keys.extend(
        [
            # mod1 + letter of group = switch to group
            Key(
                [mod],
                i.name,
                lazy.group[i.name].toscreen(),
                desc="Switch to group {}".format(i.name),
            ),
            # move window and switch
            Key(
                [mod, "shift"],
                i.name,
                lazy.window.togroup(i.name, switch_group=True),
                desc="Switch to & move window to group {}".format(i.name),
            ),
        ]
    )

